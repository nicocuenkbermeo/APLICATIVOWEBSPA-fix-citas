let currentLang = localStorage.getItem('appLang') || 'es';

function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;
    localStorage.setItem('appLang', lang);
    
    // Update active class on selector
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = translations[lang][key];
        
        if (translation) {
            // Se es un input con placeholder, traducimos el placeholder
            if (el.tagName === 'INPUT' && el.type !== 'submit') {
                // Not really using data-i18n for placeholders in this app yet, but good practice
            } else {
                el.innerHTML = translation; 
            }
        }
    });

    // Refresh dynamic content that might depend on translations
    if (document.getElementById('services-list') && document.getElementById('services-list').innerHTML.includes('No hay')) {
        document.getElementById('services-list').innerHTML = `<p>${translations[currentLang]["dash.client.empty"]}</p>`;
    }
    if (document.getElementById('therapist-appointments-list') && document.getElementById('therapist-appointments-list').innerHTML.includes('No tienes')) {
        document.getElementById('therapist-appointments-list').innerHTML = `<p>${translations[currentLang]["dash.therapist.empty"]}</p>`;
    }
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLang);
});

// Helper for dynamic translations in app.js
function t(key) {
    return translations[currentLang][key] || key;
}
